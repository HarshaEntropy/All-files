public class afksf {

        public static void main(String[] args) {
            int[][] values = { { 1, 2 },{ 4, 5}};

            System.out.println("Elements are :");
            for(int i=0; i< values.length; i++) {
                for(int j=0; j< values[i].length; j++) {
                    System.out.print(values[j][i] + "\t");
                }
                System.out.println("");
            }

        }

    }
